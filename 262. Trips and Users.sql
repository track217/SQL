解法一：使用 CASE统计满足条件个数，使用 ROUND函数保留2位小数，538ms
SELECT T.Request_at AS Day, ROUND(SUM(CASE WHEN T.Status LIKE 'cancelled_%' THEN 1 ELSE 0 END)/COUNT(*), 2) AS 'Cancellation Rate'
FROM Trips AS T INNER JOIN Users AS U ON T.Client_Id = U.Users_Id AND U.Banned='No'
WHERE T.Request_at BETWEEN '2013-10-01' AND '2013-10-03'

